    "def preprocess_images(images, target_size=(14, 14)):\n",
    "    # 1. Normalize pixel values\n",
    "    normalized = images / 255.0\n",
    "    \n",
    "    preprocessed = []\n",
    "    for img_flat in normalized:\n",
    "        # 2. Reshape to 2D image matrix\n",
    "        img_2d = img_flat.reshape(28, 28)\n",
    "        # 3. Resize using anti-aliasing\n",
    "        img_resized = skimage.transform.resize(img_2d, target_size, anti_aliasing=True)\n",
    "        # 4. Flatten back to 1D\n",
    "        preprocessed.append(img_resized.flatten())\n",
    "        \n",
    "    return np.array(preprocessed)\n",
    "\n",
    "print(\"Preprocessing training and test images...\")\n",

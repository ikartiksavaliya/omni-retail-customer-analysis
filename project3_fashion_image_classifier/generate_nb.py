    "n_components_95 = np.argmax(cumulative_variance >= 0.95) + 1\n",
    "\n",
    "plt.figure(figsize=(10, 5))\n",
    "plt.plot(range(1, len(cumulative_variance) + 1), cumulative_variance, color='royalblue', lw=2)\n",
    "plt.axhline(y=0.90, color='red', linestyle='--', label=f'90% Variance ({n_components_90} components)')\n",
    "plt.axhline(y=0.95, color='forestgreen', linestyle='--', label=f'95% Variance ({n_components_95} components)')\n",
    "plt.title(\"Cumulative Explained Variance Ratio by PCA Components\", fontsize=14)\n",
    "plt.xlabel(\"Number of Components\")\n",

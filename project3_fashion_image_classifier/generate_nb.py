    "print(\"Analyzing PCA explained variance...\")\n",
    "pca_temp = PCA(random_state=42)\n",
    "pca_temp.fit(X_train_preprocessed)\n",
    "\n",
    "# Cumulative explained variance\n",
    "cumulative_variance = np.cumsum(pca_temp.explained_variance_ratio_)\n",
